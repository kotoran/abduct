
public class Subimage {

	int tex, width, height;
	
	public Subimage(int tex, int width, int height)
	{
		this.tex = tex;
		this.width = width;
		this.height = height;
	}

	public int getTex()
	{
		return tex;
	}

	public int getWidth()
	{
		return width;
	}
	
	public int getHeight()
	{
		return height;
	}
}
